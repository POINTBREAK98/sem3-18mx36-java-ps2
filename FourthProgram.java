//THis program prints the default value of all primitive data types
public class FourthProgram {
        static char char_default;
        static int int_default;
        static float float_default;
        static double double_default;
        static long long_default;
        static  boolean bool_default;
        public static void main(String[] args) {
            System.out.println("Default value of char is "+char_default);
            System.out.println("Default value of int is "+int_default);
            System.out.println("Default value of float is "+float_default);
            System.out.println("Default value of double is "+double_default);
            System.out.println("Default value of long is "+long_default);
            System.out.println("Default value of boolean is "+bool_default);
        }
}
